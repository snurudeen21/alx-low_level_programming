#include "main.h"
#include <stdio>
/**
* print_to_98 - print from n to 98
* @n: holds the starting number
* Return: void
*/
void print_to_98(void)
{
	if (n < 98)

	{

		for (n = n; n < 98; n++)

			printf("%d, ", n);

		printf("%d\n", 98);

	}

	else

	{

		for (n = n; n > 98; n--)

			printf("%d, ", n);

		printf("%d\n", 98);

	}

}
